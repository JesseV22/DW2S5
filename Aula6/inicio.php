    <h3> O que é mercado financeiro? </h3>
    <p>

        Por definição, mercado financeiro é um ambiente de comercialização de mercadorias (produtos agrícolas, ouro),
        câmbio (moedas estrangeiras) e valores imobiliários (títulos, ações, opções).

        Diversas instituições estão envolvidas nessas negociações, que além de facilitarem a relação entre os agentes,
        também regulam e fiscalizam as transações realizadas. O papel do investidor, no mercado financeiro, é o de
        disponibilizar dinheiro com o intuito de multiplicá-lo.

        Para isso, existem diversos caminhos, mas todos partem da mesma premissa: o capital é destinado a uma
        determinada aplicação que oferece valorização com base nas diretrizes acordadas entre as partes.

        Nos títulos de renda fixa, por exemplo, o investidor pode fazer uma projeção da rentabilidade no ato do
        investimento.

        Ou seja, ele já saberá se o seu dinheiro será valorizado de forma prefixada — com um juro anual definido —
        pós-fixada, associada a um indicador, ou híbrida, na qual ele pagará um valor fixo de juros somado à variação de
        um índice de preços.

        Enquanto na renda variável, não existe uma garantia de retorno. Por exemplo, um investimento em ações de uma
        instituição na bolsa de valores pode tanto valorizar quanto desvalorizar, dependendo do interesse do mercado.
    </p>
    <h3> Mercado financeiro: para que serve?
    </h3>
    <p>
        De um lado do mercado financeiro, há os investidores, como mostramos no tópico anterior, do outro, há os
        tomadores de recursos. Eles são as instituições ou empresas que desejam captar recursos para diversos fins,
        como:

        <li>financiamento de maquinário;</li>
        <li>pagamentos de dívidas;</li>
        <li>lançamento de novos produtos ou serviços.</li>
        <li>Entre outros.</li>

            Sendo assim, podemos afirmar que uma das principais finalidades do mercado financeiro é impulsionar o fluxo
            da economia do país de forma funcional e democrática.

            Imagine que você invista R$ 10 mil em um Certificado de Depósito Bancário (CDB) em um determinado banco.
            Como retorno, você receberá juros prefixados de 10,3% ao ano, ao vencimento do título.

            Se outra pessoa procurar o mesmo banco para solicitar um financiamento de R$ 10 mil para cobrir parte do
            valor de um carro que ela esteja comprando, o que acontece? A instituição emprestará o dinheiro cobrando uma
            taxa superior à que está pagando ao investidor.

            Dessa forma, as duas vertentes da economia estão conectadas pelo mercado financeiro. As instituições
            intermediárias facilitam o relacionamento entre os investidores e os tomadores. Para isso, o intermediário
            cobra uma taxa sobre as transações.
    </p>
